#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int	i;

	i = 0;
	while (0 == 0)
	{
		if (s[i] == c % 256)
			return ((char *) s + i);
		if (s[i] == '\0')
			return (NULL);
		i++;
	}
	return (0);
}
